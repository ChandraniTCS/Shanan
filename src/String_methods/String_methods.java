package String_methods;
public class String_methods {
	public static void main(String[] args) {
		String s1= "pune";
		String s2="city";
		String s3="Beutifull";
		System.out.println(s1.length());//lenth()
		System.out.println(s2.toUpperCase());//uppercase()
		System.out.println(s3.toLowerCase());//lowercase()
		System.out.println(s1.concat(s2));//concat string()
		System.out.println(s1.substring(2));
		System.out.println(s3.substring(1,6));//substring()
		}	
	}

